package com.project.relationalopertors;

public class operators {
	public static void main(String[]args) {
		int a=50;
		int b=80;
		System.out.println(a>b);//true
		System.out.println(a<b);//false
		System.out.println(a>=b);//true
		System.out.println(a<=b);//false
		System.out.println(!(a>b));//false
		System.out.println(!(a<b));//true
	}

}
